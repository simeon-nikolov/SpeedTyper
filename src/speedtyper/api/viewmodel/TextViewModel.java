package speedtyper.api.viewmodel;

public class TextViewModel {
	private int id;
	private String text;
	
	public TextViewModel() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
